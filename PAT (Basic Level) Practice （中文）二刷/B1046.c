/*B1046
思考
	- 题意为同时喊出两个数字a1,a2,且同时比划两个数字b1,b2;
	- 谁比划出的数字正好等于两人喊出的数字之和，谁就赢了,输家罚一杯酒
	- 两人同赢或两人同输则继续下一轮，统计两人各自的杯数c1,c2 
	- 如果a1+a2=b1且a1+a2!=b2，乙输；同理，a1+a2=b2且a1+a2!=b1,甲输
	- 注意如果两人都猜不中则不计数 
*/
#include <stdio.h>
int main(){
	int N,a1,b1,a2,b2; 
	int i,c1=0,c2=0;
	scanf("%d",&N);
	for(i=0;i<N;i++){
		scanf("%d %d %d %d",&a1,&b1,&a2,&b2);
		if(a1+a2==b1&&a1+a2!=b2)	c2++; //乙输 
		if(a1+a2==b2&&a1+a2!=b1)	c1++; //甲输 
	}
	printf("%d %d\n",c1,c2);
	return 0;
}
